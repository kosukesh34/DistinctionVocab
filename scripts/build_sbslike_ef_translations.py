def main() -> None:
    with open(SBS, encoding="utf-8") as f:
        sbs = json.load(f)
    f_map = {item["number"]: item["examples"]["D"] for item in sbs}

    result: dict[str, dict[str, str]] = {}
    for n in range(1, 131):
        if n not in E_TRANSLATIONS:
            raise KeyError(f"Missing E translation for {n}")
        if n not in f_map:
            raise KeyError(f"Missing F (D) paraphrase for {n}")
        result[str(n)] = {"E": E_TRANSLATIONS[n], "F": f_map[n]}

    with open(OUT, "w", encoding="utf-8") as out:
        json.dump(result, out, ensure_ascii=False, indent=2)
        out.write("\n")

    print(f"Wrote {len(result)} entries to {OUT}")


if __name__ == "__main__":
    main()
